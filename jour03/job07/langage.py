def affiche_role_developpeur(langage):
    if langage == "JavaScript":
        print("Développeur web")
    elif langage == "python":
        print("Développeur IA")
    elif langage == "java":
        print("Développeur logiciel")
    elif langage == "reactjs":
        print("Développeur mobile")
    else:
        print("Futur meilleur développeur !")
affiche_role_developpeur("JavaScript")
affiche_role_developpeur("python")
affiche_role_developpeur("java")
affiche_role_developpeur("reactjs")
affiche_role_developpeur("C#")  
